function openNav() {
  document.getElementById("globalNav").style.width = "250px";
  document.getElementById("test").style.marginTop = "250px";
  document.body.style.backgroundColor = "rgba(0,0,0,0.4)";
}

function closeNav() {
  document.getElementById("globalNav").style.width = "0";
  document.getElementById("test").style.marginTop = "0";
  document.body.style.backgroundColor = "white";
}
